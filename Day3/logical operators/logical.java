public class logical{
   public static void main(String[] args){
    System.out.println("Logical Operators");
    System.out.println("Logical AND Operator.  Ampercent && :-");
    System.out.println((7>2) && (2>1));
    System.out.println((8>5) && (7>9));
    System.out.println((17>22) && (12>10));
    System.out.println((100>200) && (200>300));

    System.out.println("Logical OR Operator :-");
    System.out.println((7>2) || (2>1));
    System.out.println((18>5) || (7>9));
    System.out.println((7>22) || (12>10));
    System.out.println((100>200) || (200>300));

    System.out.println("Logical NOT Operator :-");
     System.out.println(!(100>200));
      System.out.println(!(500>200));
      System.out.println(!(100<200));

   }}